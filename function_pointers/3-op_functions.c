#include "3-calc.h"
/**
 * op_add - file should contain the 5
 * following functions (not more).
 * @a: integer.
 * @b: integer.
 * Return: the sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - file should contain the 5
 * following functions (not more).
 * @a: integer.
 * @b: integer.
 * Return: the difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - file should contain the 5
 * following functions (not more).
 * @a: integer.
 * @b: integer.
 * Return: the product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - file should contain the 5
 * following functions (not more).
 * @a: integer.
 * @b: integer.
 * Return: the result of the division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - file should contain the 5
 * following functions (not more).
 * @a: integer.
 * @b: integer.
 * Return: the remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
